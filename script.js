function startGame(difficulty) {
  state.difficulty = difficulty
  state.currentQ   = 0
  state.score      = 0

  showScreen('screen-game')
  loadQuestion()
}

function loadQuestion() {
  const q     = questions[state.difficulty][state.currentQ]
  const story = stories[state.difficulty][state.currentQ]

  document.getElementById('chapter-label').textContent    = story.label
  document.getElementById('story-text').textContent       = story.text
  document.getElementById('problem-equation').textContent = q.eq
  document.getElementById('feedback-text').textContent    = ''
  document.getElementById('btn-next').style.display       = 'none'
  document.getElementById('hint-text').textContent = q.hint
  document.getElementById('hint-box').style.display = 'none'  // ซ่อน hint ทุกครั้งที่โหลดข้อใหม่

  // สร้างปุ่ม choice
  const answerBox = document.getElementById('answer-box')
  answerBox.innerHTML = ''

  const labels = ['A', 'B', 'C', 'D']

  q.choices.forEach((choice, index) => {
    const btn = document.createElement('button')
    btn.textContent = `${labels[index]}. ${choice}`
    btn.onclick = () => checkAnswer(choice)
    answerBox.appendChild(btn)
  })

  MathJax.typesetPromise()
}

function checkAnswer(selected) {
  const q = questions[state.difficulty][state.currentQ]

  if (selected === q.ans) {
    state.score += 10
    document.getElementById('feedback-text').textContent = '✅ ถูกต้อง!'
  } else {
    document.getElementById('feedback-text').textContent = `❌ ผิด! คำตอบที่ถูกคือ ${q.ans}`
  }

  // ปิดปุ่มทั้งหมดหลังตอบแล้ว
  document.querySelectorAll('#answer-box button').forEach(btn => {
    btn.disabled = true
  })

  document.getElementById('btn-next').style.display = 'block'
  MathJax.typesetPromise()
}

function nextQuestion() {
  const total = questions[state.difficulty].length

  if (state.currentQ + 1 < total) {
    state.currentQ++
    loadQuestion()
  } else {
    showResult()
  }
}

function showResult() {
  showScreen('screen-result')

  const total = questions[state.difficulty].length
  document.getElementById('result-story').textContent = endings[state.difficulty]
  document.getElementById('result-score').textContent = `คะแนนของเจ้า: ${state.score} / ${total * 10}`
}

function goHome() {
  showScreen('screen-home')
}

function toggleHint() {
  const hintBox = document.getElementById('hint-box')
  if (hintBox.style.display === 'none') {
    hintBox.style.display = 'block'
  } else {
    hintBox.style.display = 'none'
  }
}


